#-----------------------------------------------------------------
# 문자열 str 데이터 다루기
#   - 문자열에서 사용 가능한 내장 함수
#-----------------------------------------------------------------

# ord() - 문자 -> 코드/기계어 변환 내장함수 
print(f'A 코드값: {ord("A")}')
print(f'가 코드값: {ord("가")}')
'''
A 코드값: 65
가 코드값: 44032
'''

# chr() - 코드/기계어 -> 문자 변환 내장함수
print(f'97의 문자 -> {chr(97)}')
print(f'65의 문자 -> {chr(65)}')
'''
97의 문자 -> a
65의 문자 -> A
'''



























