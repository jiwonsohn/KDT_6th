# [1] 산술 연산자-----------------------------------------------
# - 종류: +,-,*,/,//,%,**

num1 = 8
num2 = 3

# 출력형태: 8 + 3 = 11
print(f'{num1} + {num2} = {num1+num2}')
print(f'{num1} - {num2} = {num1-num2}')
print(f'{num1} * {num2} = {num1*num2}')
print(f'{num1} / {num2} = {num1/num2}')
print(f'{num1} // {num2} = {num1//num2}')
print(f'{num1} % {num2} = {num1%num2}')
print(f'{num1} ** {num2} = {num1**num2}')
'''
8 + 3 = 11
8 - 3 = 5
8 * 3 = 24
8 / 3 = 2.6666666666666665
8 // 3 = 2
8 % 3 = 2
8 ** 3 = 512
'''

# [2] 비교 연산자-----------------------------------------------
# - 종류: >,<,>=,<=,==,!=

num1 = 'aF'
num2 = 'ac'

print(f'{num1} > {num2} = {num1>num2}')
print(f'{num1} < {num2} = {num1<num2}')
print(f'{num1} >= {num2} = {num1>=num2}')
print(f'{num1} <= {num2} = {num1<=num2}')
print(f'{num1} == {num2} = {num1==num2}')
print(f'{num1} != {num2} = {num1!=num2}')
'''
aF > ac = False         =>
aF < ac = True
aF >= ac = False
aF <= ac = True
aF == ac = False
aF != ac = True
'''














