## [실습]-------------------------------------------------------
## 3단출력 w/ while

dan=3
n=1

while n<10:
    print(f'{dan} * {n} = {dan * n}')
    n+=1

'''
3 * 1 = 3
3 * 2 = 6
3 * 3 = 9
3 * 4 = 12
3 * 5 = 15
3 * 6 = 18
3 * 7 = 21
3 * 8 = 24
3 * 9 = 27
'''

