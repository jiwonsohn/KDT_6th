# 입력 받은 데이터에 따라 출력을 다르게

# [조건] 알파벳->★ 숫자면->♥ 나머지는 무시  <== if문

data = input("알파벳, 숫자 또는 문자 1개 입력:  ").strip()

if 'a'<= data <='z' or 'A'<= data <='Z':
    print("★")

elif '0'<= data <='9':
    print("♥")



