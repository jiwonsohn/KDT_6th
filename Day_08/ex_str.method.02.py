# strip(), lstrip(), rstrip() - 문자열 좌우 여백 제거 메서드
#                               - 문자열 내부 공백 제거 X

# strip() - 좌우 공백 제거----------------------------------------------
msg = "Good Luck"
data = "  Happy New Year 2025!   "

m1 = msg.strip()
print(f'원본 msg: {len(msg)}개 --- 공백 제거 m1: {len(m1)}개' )
# 원본 msg: 9개 --- 공백 제거 m1: 9개

m2 = data.strip()
print(f'원본 data: {len(data)}개 --- 공백 제거 m2: {len(m2)}개' )
# 원본 data: 25개 --- 공백 제거 m2: 20개


# lstrip() - 문자열 왼쪽 공백 제거----------------------------------------------
d1 = data.lstrip()
print(f'원본 data: {len(data)}개 --- 공백 제거 m2: {len(d1)}개' )
# 원본 data: 25개 --- 공백 제거 m2: 23개

# lstrip() - 문자열 오른쪽 공백 제거----------------------------------------------
d2 = data.rstrip()
print(f'원본 data: {len(data)}개 --- 공백 제거 m2: {len(d2)}개' )
# 원본 data: 25개 --- 공백 제거 m2: 22개


